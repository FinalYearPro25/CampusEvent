export default function CustomPaginationActionsTable({events}) {
    const rows = events.data;

  const [page, setPage] = React.useState(0);
  const [rowsPerPage, setRowsPerPage] = React.useState(25);

  // Avoid a layout jump when reaching the last page with empty rows.
  const emptyRows =
    page > 0 ? Math.max(0, (1 + page) * rowsPerPage - rows.length) : 0;

  const handleChangePage = (
    event: React.MouseEvent<HTMLButtonElement> | null,
    newPage: number,
  ) => {
    setPage(newPage);
  };

  const handleChangeRowsPerPage = (
    event: React.ChangeEvent<HTMLInputElement | HTMLTextAreaElement>,
  ) => {
    setRowsPerPage(parseInt(event.target.value, 10));
    setPage(0);
  };

  return (
    <TableContainer component={Paper}>
      <Table sx={{ minWidth: 500 }} aria-label="custom pagination table">
      <TableHead>
          <TableRow>
            <TableCell>Date</TableCell>
            <TableCell align="left">Title</TableCell>
            <TableCell align="left">Location</TableCell>
            <TableCell align="left">Participants Limit</TableCell>
            <TableCell align="left">Status</TableCell>

          </TableRow>
        </TableHead>
        <TableBody>
          {(rowsPerPage > 0
            ? rows.slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage)
            : rows
          ).map((row) => (
            <TableRow key={row.title}>
              <TableCell component="th" scope="row">
                {row.start_date}
              </TableCell>
              <TableCell style={{ width: 400 }} align="left">
              <Link to={`/event/${row.id}`}>
                {row.title}
                </Link>
              </TableCell>
              <TableCell style={{ width: 400 }} align="left">
                {row.location}
              </TableCell>
              <TableCell style={{ width: 150 }} align="left">
                {row.participants_limit}
              </TableCell>
              <TableCell style={{ width: 150 }} align="left">
                {(row.self == true)? "participated" : "created"}
              </TableCell>
            </TableRow>
          ))}
          {emptyRows > 0 && (
            <TableRow style={{ height: 53 * emptyRows }}>
              <TableCell colSpan={6} />
            </TableRow>
          )}
        </TableBody>
        <TableFooter>
          <TableRow>
            <TablePagination
              rowsPerPageOptions={[25, 50, 100, { label: 'All', value: -1 }]}
              colSpan={5}
              count={rows.length}
              rowsPerPage={rowsPerPage}
              page={page}
              slotProps={{
                select: {
                  inputProps: {
                    'aria-label': 'rows per page',
                  },
                  native: true,
                },
              }}
              onPageChange={handleChangePage}
              onRowsPerPageChange={handleChangeRowsPerPage}
              ActionsComponent={EventTable}
            />
          </TableRow>
        </TableFooter>
      </Table>
    </TableContainer>
  );
}
